// ── Editor interaction ────────────────────────────────────────────

inputEl.addEventListener('scroll', () => {
    qsHl.scrollTop  = inputEl.scrollTop;
    qsHl.scrollLeft = inputEl.scrollLeft;
    lnEl.scrollTop  = inputEl.scrollTop;
    updateCurLine();
});

inputEl.addEventListener('keydown', (e) => {
    if (acKeydown(e)) return;   // autocomplete has priority when popup is open

    if (e.key === 'Tab') {
        e.preventDefault();
        const s = inputEl.selectionStart;
        const v = inputEl.value;
        inputEl.value = v.slice(0, s) + '   ' + v.slice(inputEl.selectionEnd);
        inputEl.selectionStart = inputEl.selectionEnd = s + 3;
        update();
    } else if (e.key === 'Enter') {
        e.preventDefault();
        const s         = inputEl.selectionStart;
        const v         = inputEl.value;
        const lineStart = v.lastIndexOf('\n', s - 1) + 1;
        const indent    = v.slice(lineStart, s).match(/^(\s*)/)[1];
        inputEl.value   = v.slice(0, s) + '\n' + indent + v.slice(inputEl.selectionEnd);
        inputEl.selectionStart = inputEl.selectionEnd = s + 1 + indent.length;
        update();
    }
});

inputEl.addEventListener('click',  updateCurLine);
inputEl.addEventListener('keyup',  updateCurLine);
inputEl.addEventListener('select', updateCurLine);
inputEl.addEventListener('input',  () => { update(); checkAutocomplete(); });

// ── Copy / Share / Download ───────────────────────────────────────

function flashBtn(btn) {
    btn.classList.add('ok');
    setTimeout(() => btn.classList.remove('ok'), 1400);
}

btnCopy.addEventListener('click', () => {
    if (!state.lastDdlText) return;
    navigator.clipboard.writeText(state.lastDdlText).then(() => {
        const prev = btnCopy.textContent;
        btnCopy.textContent = '✓ copied';
        flashBtn(btnCopy);
        setTimeout(() => { btnCopy.textContent = prev; }, 1400);
    });
});

btnShare.addEventListener('click', () => {
    if (state.activeTab === 'erd') capturePositions();
    let fragment;
    if (state.lastErdPos.size > 0 || state.collapsed.size > 0) {
        const data = { q: inputEl.value };
        if (state.lastErdPos.size > 0) data.p = [...state.lastErdPos.entries()];
        if (state.collapsed.size > 0)  data.c = [...state.collapsed];
        fragment = 'v2:' + encodeURIComponent(JSON.stringify(data));
    } else {
        fragment = encodeURIComponent(inputEl.value);
    }
    const url = window.location.href.split('#')[0] + '#' + fragment;
    navigator.clipboard.writeText(url).then(() => {
        const prev = btnShare.textContent;
        btnShare.textContent = '✓ link copied';
        flashBtn(btnShare);
        setTimeout(() => { btnShare.textContent = prev; }, 1400);
    });
});

document.getElementById('btn-download').addEventListener('click', () => {
    if (!state.lastDdlText) return;
    const blob     = new Blob([state.lastDdlText], { type: 'text/plain' });
    const a        = document.createElement('a');
    a.href         = URL.createObjectURL(blob);
    const ts       = new Date().toISOString().slice(0, 19).replace(/[T:]/g, '-');
    a.download     = DIFF_DELIMITER.test(inputEl.value) ? `migration-${ts}.sql` : `schema-${ts}.sql`;
    a.click();
    URL.revokeObjectURL(a.href);
});

document.getElementById('btn-compare').addEventListener('click', () => {
    if (DIFF_DELIMITER.test(inputEl.value)) return;
    inputEl.value += '\n\n# ---\n\n';
    inputEl.selectionStart = inputEl.selectionEnd = inputEl.value.length;
    inputEl.focus();
    update();
});

