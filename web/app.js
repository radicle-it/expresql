const DIFF_DELIMITER = /^#\s*-{2,}\s*$/m;

function makeFooter(input) {
    const safe = input.replace(/\/\*/g, '--<--').replace(/\*\//g, '-->--');
    return `\n-- Generated by Radicle QuickSQL ${qsql_version()} ${new Date().toLocaleString()}\n\n/*\n${safe}\n\n*/`;
}

function settingsSummary(src) {
    const m = /^#\s*settings\s*=\s*\{([^}]*)\}/im.exec(src);
    if (!m) return '';
    const body   = m[1];
    const get    = (key) => new RegExp(`\\b${key}\\s*[=:]\\s*["']?([^,}"'\\s]+)`, 'i').exec(body)?.[1];
    const parts  = [];
    const prefix = get('prefix');
    const db     = get('db');
    const pk     = get('pk');
    const api    = get('api');
    if (prefix && prefix !== 'null') parts.push(`prefix: ${prefix}`);
    if (db)                          parts.push(`db: ${db}`);
    if (pk)                          parts.push(`pk: ${pk}`);
    if (api)                         parts.push(`api: ${api}`);
    return parts.length ? '  ·  ' + parts.join('  ·  ') : '';
}

function updateCurLine() {
    if (!curLineEl) return;
    const s       = inputEl.selectionStart;
    const lineIdx = (inputEl.value.slice(0, s).match(/\n/g) || []).length;
    curLineEl.style.top = (14 + lineIdx * 13 * 1.65 - inputEl.scrollTop) + 'px';
}

// Flag set when typing while DDL tab is hidden — forces re-render on tab switch.
let ddlStale = false;

function renderDdl(ddl) {
    outputEl.innerHTML = highlightSQL(ddl) + '\n ';
    ddlStale = false;
}

function update() {
    const src   = inputEl.value;
    const lines = src.split('\n').length;
    lnEl.textContent = Array.from({ length: lines }, (_, i) => i + 1).join('\n');
    qsHl.innerHTML   = highlightQuickSQL(src) + '\n ';
    updateCurLine();

    const curTab = state.tabs.find(t => t.id === state.activeSchemaTabId);
    if (curTab) { curTab.qsql = src; saveTabs(); }

    const delimMatch = DIFF_DELIMITER.exec(src);

    try {
        let outputText;
        if (delimMatch) {
            const v1     = src.slice(0, delimMatch.index);
            const v2     = src.slice(delimMatch.index + delimMatch[0].length);
            const result = toDiff(v1, v2);
            outputText   = result.sql + makeFooter(src);
            const s      = result.summary;
            const manual = s.statementsRequiringIntervention;
            btnCopy.textContent  = '⧉ Copy Migration';
            statusEl.textContent = `Migration: +${s.tablesAdded} · ~${s.tablesModified} modified · ${s.statementsTotal} statements` +
                (manual ? ` · ⚠ ${manual} manual` : '') + settingsSummary(v2);
            statusEl.className = 'status-active';
            const warns = result.warnings;
            migWarningsEl.innerHTML = (warns.length > 0 ? '<div class="mig-warn-hdr">⚠ Migration notices</div>' : '') +
                warns.map(w => {
                    const cls = w.level === 'DESTRUCTIVE' ? 'mig-warn-destructive'
                              : w.level === 'LOSSY'        ? 'mig-warn-lossy'
                              :                              'mig-warn-info';
                    const loc = w.column ? `[${w.table}.${w.column}]` : `[${w.table}]`;
                    return `<div class="mig-warn ${cls}">&#9888; ${w.level}  ${loc}  ${w.message}</div>`;
                }).join('');
        } else {
            outputText           = toDDL(src);
            btnCopy.textContent  = '⧉ Copy';
            statusEl.textContent = `${outputText.split('\n').length} DDL lines generated${settingsSummary(src)}`;
            statusEl.className   = 'status-active';
            migWarningsEl.innerHTML = '';
        }
        state.lastDdlText     = outputText;
        errorEl.style.display = 'none';
        // Skip expensive highlight pass when DDL panel is not visible.
        if (state.activeTab === 'ddl') {
            renderDdl(outputText);
        } else {
            ddlStale = true;   // will be flushed on tab switch
        }
    } catch (e) {
        state.lastDdlText       = '';
        errorEl.textContent     = e.message || String(e);
        errorEl.style.display   = 'block';
        statusEl.textContent    = 'syntax error';
        statusEl.className      = 'status-error';
        btnCopy.textContent     = '⧉ Copy';
        migWarningsEl.innerHTML = '';
    }

    if (state.activeTab === 'erd') {
        if (state.erdDebounceTimer) clearTimeout(state.erdDebounceTimer);
        state.erdDebounceTimer = setTimeout(() => {
            capturePositions();
            state.erdDebounceTimer = null;
            updateDiagram(true);
        }, 350);
    }
}

